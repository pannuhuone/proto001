Router.configure({
    layoutTemplate: 'masterLayout',
    loadingTemplate: 'loading',
    notFoundTemplate: 'pageNotFound',
    yieldTemplates: {
        nav: {to: 'nav'},
        footer: {to: 'footer'},
    }
});

Router.map(function() {
    this.route('home', {
        path: '/',
    });

    this.route('private');
    this.route('profile');
});

Router.plugin('ensureSignedIn', {
  only: ['private']
});

Router.route('/sign-out', {
    onBeforeAction: function() {
        Meteor.logout(function() {
        });
        Router.go('home');
    }
});

